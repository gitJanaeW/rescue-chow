import React from "react";
import Auth from "../utils/shopping/auth";
import Img from "../assets/favicon.ico";
import { Fragment } from "react";
import { Disclosure, Menu, Transition } from "@headlessui/react";
import { Bars3Icon, BellIcon, XMarkIcon } from "@heroicons/react/24/outline";
import dropdownImg from "../assets/icons8-double-down-50.png";
import { idbPromise } from "../utils/helpers";
import { useStoreContext } from "../utils/shopping/GlobalState";
import { TOGGLE_CART } from "../utils/shopping/actions";

const user = {
  name: "user",
  email: "user@email.com",
  imageUrl: { dropdownImg },
};
const navigation = [
  { name: "Home", href: "/", current: false },
  { name: "Who We Are", href: "/who-we-are", current: false },
  { name: "Order Now", href: "/shop", current: false },
  { name: "Find A Rescue", href: "/find-a-rescue", current: false },
  { name: "Get In Touch", href: "/get-in-touch", current: false },
];

let userNavigation = [];

function Logout() {
  async function clearCart() {
    const cart = await idbPromise("cart", "get");
    if (cart.length) {
      cart.forEach((item) => {
        idbPromise("cart", "delete", item);
      });
    }
    setTimeout(() => {
      window.location.assign("/");
    }, 3000);
  }

  clearCart();
  Auth.logout();
}

function showNavigation() {
  if (Auth.loggedIn()) {
    userNavigation = [
      ...userNavigation,
      { name: "Logout", href: "/", onclick: Logout },
      { name: "Order History", href: "/orderHistory" },
    ];
  } else {
    userNavigation = [
      ...userNavigation,
      { name: "Login", href: "/login" },
      { name: "Signup", href: "/signup" },
    ];
  }
}
showNavigation();

function classNames(...classes) {
  return classes.filter(Boolean).join(" ");
}

